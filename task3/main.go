package main

import "fmt"

// Функция для определения, может ли данная строка быть сформирована после описанных операций
func canFormString(s string) string {
	// Переменная для хранения предыдущего символа
	var prevChar rune
	// Переменная для хранения количества повторений подряд идущих символов
	count := 0

	// Проходим по каждому символу в строке
	for _, char := range s {
		// Если текущий символ равен предыдущему, увеличиваем счетчик
		if char == prevChar {
			count++
		} else {
			// Если количество повторений предыдущего символа нечетное, возвращаем "NO"
			if count%2 != 0 {
				return "NO"
			}
			// Обновляем предыдущий символ и счетчик
			prevChar = char
			count = 1
		}
	}

	// Проверяем последний символ
	if count%2 != 0 {
		return "NO"
	}

	// Если все символы удовлетворяют условиям, возвращаем "YES"
	return "YES"
}

func main() {
	var t int
	fmt.Scan(&t) // Ввод числа тестов

	// Цикл по всем тестам
	for i := 0; i < t; i++ {
		var s string
		fmt.Scan(&s) // Ввод строки

		// Проверка и вывод результата
		fmt.Println(canFormString(s))
	}
}
